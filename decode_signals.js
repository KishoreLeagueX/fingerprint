// Extract and decode the iO signal name array

function _T(A, N) {
    return (A - N + 256) % 256
}

function qe(A) {
    return A instanceof ArrayBuffer ? new Uint8Array(A) : new Uint8Array(A.buffer, A.byteOffset, A.byteLength)
}

function WT(A, N, v) {
    const Y = () => { throw new Error("Invalid data") };
    const q = qe(A);
    q.length < N.length + 2 && Y();
    for (let A = 0; A < N.length; ++A)
        _T(q[1 + A], q[0]) !== N[A] && Y();
    const W = 1 + N.length;
    const H = _T(q[W], q[0]);
    q.length < W + 1 + H + v && Y();
    const l = W + 1 + H;
    const T = l + v;
    const U = new ArrayBuffer(q.length - T);
    const e = new Uint8Array(U);
    for (let A = 0; A < e.length; ++A)
        e[A] = q[T + A] ^ q[l + A % v];
    return U
}

